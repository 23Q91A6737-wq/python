# Area of a Right Angled Triangle

import math

a = float(input("Enter side A : "))
b = float(input("Enter side B : "))

# square root if the right angled triangle =

# = opposite side^2 + adjacent side^2 = Hypotenuse side^2

# the below step is square root of right angled triangle is

# square root of a^2 + b^2

c = math.sqrt(pow(a,2)+pow(b,2))

print(f"The area of Hypotenuse of a right angled triangle is : {c} cm^2")